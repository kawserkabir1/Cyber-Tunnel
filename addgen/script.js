generateBtn.addEventListener('click', () => {
  const selectedCountry = countrySelect.value;

  if (addressData[selectedCountry]) {
    const randomIndex = Math.floor(Math.random() * addressData[selectedCountry].length);
    const randomAddress = addressData[selectedCountry][randomIndex];

    addressOutput.innerHTML = ''; // Clear previous output

    // Create elements for each address component with labels
    const streetElement = document.createElement('p');
    streetElement.innerHTML = `<b>Street:</b> ${randomAddress.street}`;
    addressOutput.appendChild(streetElement);

    const cityElement = document.createElement('p');
    cityElement.innerHTML = `<b>City/Town:</b> ${randomAddress.city}`;
    addressOutput.appendChild(cityElement);

    // Add the state element
    const stateElement = document.createElement('p');
    stateElement.innerHTML = `<b>State:</b> ${randomAddress.state}`;
    addressOutput.appendChild(stateElement);

    const zipElement = document.createElement('p');
    zipElement.innerHTML = `<b>Zip/Postal Code:</b> ${randomAddress.zip}`;
    addressOutput.appendChild(zipElement);

  } else {
    addressOutput.textContent = "No addresses found for this country 😔";
  }
});

